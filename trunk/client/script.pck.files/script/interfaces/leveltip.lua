function Win_LevelTip:CountShowTip(ntip, ncontent, np, nn)
	local stip = {}
	local scontent = {}
	for z = 1, numberperpage do
		k = (np - 1) * numberperpage + z
		if k > nn then break end
		stip[z] = ntip[k]
		scontent[z] = ncontent[k]
	end
	return stip, scontent
end

function Win_LevelTip:CountFilterNum()
	local ischeck1,ischeck2
		ischeck1 = DlgApi.IsItemChecked(self.this, "Chk_Filter1")
		ischeck2 = DlgApi.IsItemChecked(self.this, "Chk_Filter2")
		if ischeck2 then fnum = 3
		elseif ischeck1 then fnum = 2
