--fnum	1	全部显示
--fnum	2	显示所有可用
--fnum	3	只显示当前等级
function Win_LevelTip:FilterTips(lv, fnum, prof)
	local AllTips = {}
	local AllContent = {}
	local num = 0
	local ShowLv = ""
	local Main_Rank = 0
	local Main_Class = 0
	local maxpage = 1

	if fnum == 2 then
    	for k, v in ipairs(LevelTip) do
    		if v.Level <= lv then
       		    if prof == v.Prof or v.Prof == 0 then
            		num = num + 1
                    if v.Level <= 1 then
                        ShowLv = AUI_LEVELTIP_NOLEVEL
                    else
                        ShowLv = Format("%d%s",v.Level,AUI_LEVELTIP_LEVELSUFFIX)
                    end

                    if v.Prof == 0 or v.ProfLv == 0 then
                       ShowProfLv =  AUI_LEVELTIP_NOPROF
                    else
                     	for i = 1, Max_iLevel do
                    	    if v.ProfLv >= Prof0ClassLevel[i] then
                    	        Main_Rank = i
                    	    else Main_Rank = i - 1 break end
                    	end
                    	Main_Class = v.ProfLv - Prof0ClassLevel[Main_Rank] + 1

                    	ShowProfLv = Format("%s%s", ProfFirstName[v.Prof][Main_Rank], ProfNextName[Main_Class])
                    end

                    AllTips[num] = Format("%s\t%s\t%s\t%s", LevelTipType[v.Type], ShowLv, ShowProfLv, v.Title)
                    AllContent[num] = v.Content
            	end
    		end
    	end
		maxpage = math.floor(num/numberperpage)+1
	elseif fnum == 3 then
    	for k, v in ipairs(LevelTip) do
    		if v.Level == lv then
    		    if prof == v.Prof or v.Prof == 0 then
            		num = num + 1
                    if v.Level <= 1 then
                        ShowLv = AUI_LEVELTIP_NOLEVEL
                    else
                        ShowLv = Format("%d%s",v.Level,AUI_LEVELTIP_LEVELSUFFIX)
                    end

                    if v.Prof == 0 or v.ProfLv == 0 then
                       ShowProfLv =  AUI_LEVELTIP_NOPROF
                    else
                     	for i = 1, Max_iLevel do
                    	    if v.ProfLv >= Prof0ClassLevel[i] then
                    	        Main_Rank = i
                    	    else Main_Rank = i - 1 break end
                    	end
                    	Main_Class = v.ProfLv - Prof0ClassLevel[Main_Rank] + 1
                    	ShowProfLv = Format("%s%s", ProfFirstName[v.Prof][Main_Rank], ProfNextName[Main_Class])
                    end

                    AllTips[num] = Format("%s\t%s\t%s\t%s", LevelTipType[v.Type], ShowLv, ShowProfLv, v.Title)
                    AllContent[num] = v.Content
            	end
            end
		end
		maxpage = math.floor(num/numberperpage)+1
	elseif fnum == 1 then
    	for k, v in ipairs(LevelTip) do
    		if v.Level <= Max_Level then
    		    if prof == v.Prof or v.Prof == 0 then
            		num = num + 1
                    if v.Level <= 1 then
                        ShowLv = AUI_LEVELTIP_NOLEVEL
                    else
                        ShowLv = Format("%d%s",v.Level,AUI_LEVELTIP_LEVELSUFFIX)
                    end


                    if v.Prof == 0 or v.ProfLv == 0 then
                       ShowProfLv =  AUI_LEVELTIP_NOPROF
                    else
                     	for i = 1, Max_iLevel do
                    	    if v.ProfLv >= Prof0ClassLevel[i] then
                    	        Main_Rank = i
                    	    else Main_Rank = i - 1 break end
                    	end
                    	Main_Class = v.ProfLv - Prof0ClassLevel[Main_Rank] + 1
                    	ShowProfLv = Format("%s%s", ProfFirstName[v.Prof][Main_Rank], ProfNextName[Main_Class])
                    end

                    AllTips[num] = Format("%s\t%s\t%s\t%s", LevelTipType[v.Type], ShowLv, ShowProfLv, v.Title)
                    AllContent[num] = v.Content
                end
			end
    	end
		maxpage = math.floor(num/numberperpage)+1
    else
        num = 0
		maxpage = 0
	end
	return AllTips,AllContent,num, maxpage
end

function Win_LevelTip:Tick(deltaTime)
    local times = deltaTime + 1000

   	if DlgApi.IsDialogShow(self.this) then
	   if times  > 1000 then
		   if DlgApi.GetItemText(self.this, "Edt_Search") == "" then
					DlgApi.EnableItem(self.this, "Btn_Search", false)
		   else
			  DlgApi.EnableItem(self.this, "Btn_Search", true)
		   end
		   times = times - 1000
	   end
	end

	local LevelHelp3 = "Win_LevelHelp3"
	local choosebtn = DlgApi.GetCheckedRadioButton(self.this, 1)
	if choosebtn == 4 then
		DlgApi.ShowDialog(self.this, false)
		DlgApi.ShowDialog(LevelHelp3, true)
	end
end

